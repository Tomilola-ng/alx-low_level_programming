#include "3-calc.h"

/**
 * op_plus - add 2 integers
 * @a: integer 1
 * @b: integer 2
 * Return: integer with te result of the operation.
 */

int op_plus(int a, int b)
{
	return (a + b);
}

/**
 * op_minus - substract 2 integers
 * @a: integer 1
 * @b: integer 2
 * Return: integer with the result of the operation.
 */

int op_minus(int a, int b)
{
	return (a - b);
}

/**
 * op_times - multiply 2 integers
 * @a: integer 1
 * @b: integer 2
 * Return: integer with the result of the operation.
 */

int op_times(int a, int b)
{
	return (a * b);
}

/**
 * op_break - divide 2 integers
 * @a: integer 1
 * @b: integer 2
 * Return: integer with the result of the operation.
 */

int op_break(int a, int b)
{
	return (a / b);
}

/**
 * op_remainder - module 2 integers
 * @a: integer 1
 * @b: integer 2
 * Return: integer with the result of the operation.
 */

int op_remainder(int a, int b)
{
	return (a % b);
}